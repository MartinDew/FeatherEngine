-- ---- Shared codegen function --------------------------------------------
-- Runs both Python code generation scripts before any source file compiles.
-- Python scripts use write_if_changed() internally, so re-runs are cheap.
local function run_codegen(target)
    local proj = os.projectdir()
    cprint("${cyan}[codegen]${reset} generate_core_registers.py")
    os.vrunv("python3", {
        path.join(proj, "tools", "generate_core_registers.py"),
        "--core-path", path.join(proj, "core"),
    }, {curdir = proj})
    cprint("${cyan}[codegen]${reset} generate_embedded_resources.py")
    os.vrunv("python3", {
        path.join(proj, "tools", "generate_embedded_resources.py"),
    }, {curdir = proj})
end

-- ---- Per-toolchain compile flags helper ---------------------------------
local function apply_compile_flags(target)
    local want_lto     = has_config("production") or has_config("use_lto")
    local want_static  = has_config("production") or has_config("static_cpp")

    -- LLD linker on POSIX with LLVM toolchain
    if is_toolchain("llvm") and not is_plat("windows", "macosx") then
        target:add("ldflags", "-fuse-ld=lld", {force = true})
    end

    -- ThinLTO on non-MSVC
    if want_lto and not is_toolchain("msvc", "clang-cl") then
        target:add("cxflags", "-flto=thin", {force = true})
        target:add("ldflags", "-flto=thin", {force = true})
    end

    -- Static libstdc++ on Linux
    if want_static and is_plat("linux") then
        target:add("ldflags", "-static-libgcc", "-static-libstdc++", {force = true})
    end

    -- Sanitizers (debug + non-MSVC only)
    if has_config("enable_sanitizers") and is_mode("debug") and not is_toolchain("msvc", "clang-cl") then
        target:add("cxflags", "-fsanitize=address,undefined", "-fno-omit-frame-pointer", {force = true})
        target:add("ldflags", "-fsanitize=address,undefined", {force = true})
    end

    if is_toolchain("msvc", "clang-cl") then
        target:add("cxflags", "/W4", "/wd4100", {force = true})
        if is_mode("debug") then
            target:add("cxflags", "/Od", "/Zi", {force = true})
        elseif is_mode("releasedbg") then
            target:add("cxflags", "/O2", "/Zi", {force = true})
        elseif is_mode("release") then
            target:add("cxflags", "/O2", {force = true})
        end
    else
        target:add("cxflags",
            "-Wall", "-Wextra", "-pedantic", "-Wno-unused-parameter",
            {force = true})
        if is_mode("debug") then
            target:add("cxflags", "-g", "-O0", {force = true})
        elseif is_mode("releasedbg") then
            target:add("cxflags", "-g", "-O2", {force = true})
        elseif is_mode("release") then
            target:add("cxflags", "-O3", {force = true})
        end
    end
end

-- ---- Main executables ---------------------------------------------------
for _, variant in ipairs({"editor", "standalone"}) do
    target("feather." .. variant)
        set_kind("binary")
        set_basename("feather." .. variant)
        set_targetdir("$(builddir)/bin")
        add_files(CORE_SOURCES)
        -- modules.gen.cpp is hand-authored (despite the .gen extension); include as regular source
        add_files("modules/modules.gen.cpp")
        add_includedirs("$(projectdir)", "$(projectdir)/core")

        -- EDITOR_BUILD define: PUBLIC on editor so module libs compiled into this exe see it.
        -- Matches CMake: Editor uses PUBLIC, Standalone uses PRIVATE.
        if variant == "editor" then
            add_defines("EDITOR_BUILD=1", {public = true})
        else
            add_defines("EDITOR_BUILD=0")
        end

        -- BETA in debug + releasedbg (CMake Development), absent in release
        if is_mode("debug", "releasedbg") then
            add_defines("BETA")
        end
        -- NDEBUG: xmake's mode.releasedbg adds NDEBUG by default, but CMake's Development
        -- config does not define it. Remove it to match CMake behaviour.
        if is_mode("releasedbg") then
            remove_defines("NDEBUG")
        end

        -- PRODUCTION for release builds (mirrors CMake option)
        if is_mode("release") then
            add_defines("PRODUCTION")
        end

        -- Third-party deps (private: consumers of the engine don't need these headers)
        add_deps("feather_public_api")
        add_packages("flecs", "assimp", "sdl3", "taywee_args")

        -- Code generation: run both Python scripts before compilation
        before_build(run_codegen)

        -- Copy shaders directory next to the executable after every build
        after_build(function(t)
            os.cp(
                path.join(os.projectdir(), "raw_resources", "shaders"),
                path.join(t:targetdir(), "shaders"))
        end)

        on_load(apply_compile_flags)

        -- Windows symbol export for runtime plugin/module loading.
        -- CMake used WINDOWS_EXPORT_ALL_SYMBOLS + ENABLE_EXPORTS on the executables.
        -- Phase 1: point at a pre-generated .def file extracted from the CMake build.
        --   1. Build once with CMake: cmake --preset multi && cmake --build --preset multi-development
        --   2. Extract: dumpbin /EXPORTS build\multi\bin\Development\feather.editor.exe
        --   3. Keep only the EXPORTS section, save as tools/feather.{variant}.def
        --   4. Uncomment the add_ldflags line below.
        -- Phase 2 (long-term): add FEATHER_API __declspec(dllexport/import) decorators.
        --
        -- if is_plat("windows") then
        --     add_ldflags(
        --         "/DEF:" .. path.join(os.projectdir(), "tools", "feather." .. variant .. ".def"),
        --         {force = true})
        -- end
    target_end()
end

